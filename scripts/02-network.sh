#!/bin/bash
# ═══════════════════════════════════════════════════════════════════════════
# 02-network.sh  •  Network interface config and fix-eth watchdog
# ═══════════════════════════════════════════════════════════════════════════
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "$SCRIPT_DIR/lib.sh"
check_root

log INFO "=== [02] Настройка сети ==="

: "${IP_ADDRESS:=192.168.1.136}"
: "${NETMASK:=255.255.255.0}"
: "${GATEWAY:=192.168.1.1}"
: "${DNS_SERVERS:=77.88.8.8 77.88.8.1}"
: "${NET_IFACE:=eth0}"

# ── /etc/network/interfaces ────────────────────────────────────────────────
log INFO "Создание /etc/network/interfaces"
cat > /etc/network/interfaces <<NET
# Generated by СА-02м installer
source /etc/network/interfaces.d/*

auto lo
iface lo inet loopback
NET

# ── eth0 static config ────────────────────────────────────────────────────
mkdir -p /etc/network/interfaces.d
log INFO "eth0 → $IP_ADDRESS / $NETMASK"
cat > /etc/network/interfaces.d/eth0.conf <<ETH0
auto eth0
iface eth0 inet static
    address $IP_ADDRESS
    netmask $NETMASK
    gateway $GATEWAY
    dns-nameservers $DNS_SERVERS
ETH0

# ── Network watchdog deployment ────────────────────────────────────────────
ETC_DIR="$SCRIPT_DIR/../etc"

if [ -f "$ETC_DIR/fix-eth.sh" ]; then
    log INFO "Установка fix-eth.sh"
    install -m 755 "$ETC_DIR/fix-eth.sh" /usr/local/bin/fix-eth.sh
fi

if [ -f "$ETC_DIR/net-watchdog.sh" ]; then
    log INFO "Установка net-watchdog.sh"
    install -m 755 "$ETC_DIR/net-watchdog.sh" /usr/local/bin/net-watchdog.sh
fi

if [ -f "$ETC_DIR/fix-eth.service" ]; then
    log INFO "Установка fix-eth.service"
    install -m 644 "$ETC_DIR/fix-eth.service" /etc/systemd/system/fix-eth.service
fi

if [ -f "$ETC_DIR/net-watchdog.service" ]; then
    log INFO "Установка net-watchdog.service"
    install -m 644 "$ETC_DIR/net-watchdog.service" /etc/systemd/system/net-watchdog.service
fi

if [ -f "$ETC_DIR/99-lan-recovery.rules" ]; then
    log INFO "Установка udev правил"
    install -m 644 "$ETC_DIR/99-lan-recovery.rules" /etc/udev/rules.d/
fi

# Шаблон конфига watchdog (только если ещё не существует — не затираем пользовательский)
if [ -f "$ETC_DIR/sa02m_network.conf" ] && [ ! -f /etc/sa02m_network.conf ]; then
    log INFO "Создание /etc/sa02m_network.conf (шаблон)"
    install -m 644 "$ETC_DIR/sa02m_network.conf" /etc/sa02m_network.conf
fi

systemctl daemon-reload
udevadm control --reload-rules 2>/dev/null || true
svc_enable net-watchdog
log OK "Network watchdog активирован"

# ── Apply now ─────────────────────────────────────────────────────────────
log INFO "Поднимаем $NET_IFACE"
ip link set "$NET_IFACE" up 2>/dev/null || true
ip addr flush dev "$NET_IFACE" 2>/dev/null || true
ifup "$NET_IFACE" >> "$LOG_FILE" 2>&1 || true

log OK "=== [02] Сеть настроена: $IP_ADDRESS ==="
